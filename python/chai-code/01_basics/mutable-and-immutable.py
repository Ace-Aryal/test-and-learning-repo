# # 🧠 Final Conclusion

# * In Python, **variables store references, not actual values**
# * `x = 10`, `y = 10` → both usually point to the **same object** (due to caching for some small strings and integers)
# * When you do `x = 11` → a **new object is referenced**, `y` is unaffected, and value is never changed in-place only reference is updated

# ---

# # 🔒 Immutable

# * Cannot be changed
# * Any update → **new object created**

# ---

# # ✏️ Mutable

# * Can be changed **in-place**
# * Same object is modified

# ---

# # 🔥 One-liner to remember

# > Variables point to objects, not values — and behavior depends on mutability.

# ---

# If you keep this mental model clear, a lot of Python confusion just disappears 👍
